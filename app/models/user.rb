  def email_verified?
    self.email && self.email !~ TEMP_EMAIL_REGEX
  end

  private

  def lat_changed?
    if (self.location_changed?)
        if !(self.latitude_changed?)
            self.errors.add(:location, 'Location is not valid')
            return false
        end
    end
    return true
  end

end
